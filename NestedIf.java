package HundredDayCode;

public class NestedIf {
	public static void main(String[] args) {
		int num=-10;
		
		if(num>=0)
		{
			if(num==0)
				System.out.println("zero");
			else
				System.out.println("The Number is Positive");
		}
		else
			System.out.println("The number is Negative");
			
	}

}
